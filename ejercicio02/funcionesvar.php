<?php
function calSuma( $valor1, $valor2){
    $resultado = $valor1 + $valor2;
    return $resultado;
}
function calResta( $valor1, $valor2){
    $resultado = $valor1 - $valor2;
    return $resultado;
}
function calMultipicacion( $valor1, $valor2){
    $resultado = $valor1 * $valor2;
    return $resultado;
}
function calDibision( $valor1, $valor2){
    $resultado = $valor1 / $valor2;
    return $resultado;
}
function calResto( $valor1, $valor2){
    $resultado = $valor1 % $valor2;
    return $resultado;
}
function calElevado( $valor1, $valor2){
    $resultado = $valor1 ** $valor2;
    return $resultado;
}
?>